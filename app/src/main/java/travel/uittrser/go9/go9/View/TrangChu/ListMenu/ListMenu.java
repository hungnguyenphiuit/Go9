package travel.uittrser.go9.go9.View.TrangChu.ListMenu;

public class ListMenu {
    private String Ten;
    private int Hinh;

    public ListMenu(String ten, int hinh) {
        Ten = ten;
        Hinh = hinh;
    }

    public String getTen() {
        return Ten;
    }

    public void setTen(String ten) {
        Ten = ten;
    }

    public int getHinh() {
        return Hinh;
    }

    public void setHinh(int hinh) {
        Hinh = hinh;
    }
}
